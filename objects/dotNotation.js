// 1. Using dot Notation
// syntax: objectName.key

const person = {
    name: 'John',
    age: 20,
};

// accessing property
console.log(person.name) // John
console.log(person.age) // 20


// 2. Using bracket Notation
// syntax: objectName["propertyName"]

const student = { 
    name: 'John', 
    age: 20, 
};

// accessing property
console.log(student["name"]); // John
