/**
 * Create a class User that fits the sample usage. 
 * Capture the constructor params into 3 instance variables firstName, lastName, and age.
 */

/**
 * @param {firstName} string
 * @param {lastName} string
 * @param {age} number
 */

class User {

    constructor(firstName, lastName, age) {

        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
    }
}

